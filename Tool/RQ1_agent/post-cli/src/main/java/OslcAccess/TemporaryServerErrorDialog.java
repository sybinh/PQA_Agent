    public TemporaryServerErrorDialog(TemporaryServerError serverError) {
        super(EcvApplication.getMainWindow(), true);
        UiThreadChecker.ensureDispatchThread();
        assert (serverError != null);
        this.serverError = serverError;
        this.result = DialogResult.CANCEL;
        initComponents();
        getRootPane().setDefaultButton(cancelButton);
        messageArea.setText(serverError.getMessageForUi());
        setLocationRelativeTo(EcvApplication.getMainWindow());
        
        //
        // Activate close on Escape
        //
        getRootPane().registerKeyboardAction(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                result = DialogResult.CANCEL;
                setVisible(false);
                dispose();
            }
        }, KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0), JComponent.WHEN_IN_FOCUSED_WINDOW);
        
    }

    public DialogResult getResult() {
        return (result);
    }

